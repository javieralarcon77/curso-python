"""
la funcion input solicita un dato por 
consola y retorna siempre un string
"""
nombre = input("Ingrese el nombre: ")

edad = input("Ingresa tu edad: ")
"""
para convertir un string 
en un entero  usamos la funcion int
en un decimal usamos la funcion float
"""
edad = int(edad)

print(nombre)
print(edad)